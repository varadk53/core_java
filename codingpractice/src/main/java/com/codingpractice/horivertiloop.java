package com.codingpractice;

public class horivertiloop {
	
	public static void main(String[] args) {

	String name = "vardak";
	
	int count = 0;
	
	// print name horizontally
	
	while (count < 10) {
		System.out.print(name + " ");
		
		count++;
		
	}
	System.out.println();
	
	// reset count
	
	count = 0;
	// print name vertically
	while (count < 10) {
		System.out.println(name);
		
		count++;
		
	   } 
	
	}
}
